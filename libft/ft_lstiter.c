#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst)
		return ;
	while (lst->next)
	{
		f((void *)lst->content);
		lst = lst->next;
	}
	f((void *)lst->content);
	return ;
}
