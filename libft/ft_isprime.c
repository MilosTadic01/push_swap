int	ft_isprime(int nb)
{
	int	i;
	int	j;

	i = 2;
	if (nb < 2)
		return (0);
	while (i * i <= nb)
	{
		j = 2;
		while (j <= i)
		{
			if (j * i == nb)
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}

